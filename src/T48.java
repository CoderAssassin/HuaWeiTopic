public class T48 {

    public static void main(String[] args){
        Scanner sc=new Scanner(System.in);
        while (sc.hasNext()){
            int N=sc.nextInt();
            for (int i=0;i<N;i++){
                System.out.print("ABCD");
            }
            System.out.println();
        }
    }
}
